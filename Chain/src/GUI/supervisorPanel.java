                ad=new Product(prodbrand.getText(),prodtype.getText(),Double.parseDouble(prodcost.getText()),Double.parseDouble(prodprice.getText()),Integer.parseInt(prodstock.getText()),infoPerson.getPlace(),prodcat.getSelectedItem().toString(),"2050-12-12");
